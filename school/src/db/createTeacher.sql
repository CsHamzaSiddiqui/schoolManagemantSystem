CREATE TABLE teacher (
    id            INTEGER      PRIMARY KEY,
    personalNo            INTEGER,
    orderNo            INTEGER,
    name          VARCHAR (25),
    ph            VARCHAR (15),
    CNIC          VARCHAR (20),
    address       VARCHAR (50),
    education     VARCHAR (20),
    spouseName     VARCHAR (20),
    spouseCNIC     VARCHAR (20),
    emailAddress     VARCHAR (20),
    cast     VARCHAR (20),
    religion     VARCHAR (20),
    BPS     VARCHAR (20),
    created          DATE,
    DOB              DATE,
    DOJ              DATE,
    DOR              DATE,
    image         BLOB,
    deleted       BOOLEAN
);